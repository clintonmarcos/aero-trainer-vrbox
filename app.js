function drawYoke(x, y, size) {
  ctx.strokeStyle = "#d8e4e7";
  ctx.lineWidth = 5;
  ctx.beginPath();
  ctx.arc(x, y, size, Math.PI * 0.15, Math.PI * 0.85, true);
  ctx.stroke();
  ctx.beginPath();
  ctx.moveTo(x, y);
  ctx.lineTo(x, y + size * 0.9);
  ctx.stroke();
}

function arrow(x1, y1, x2, y2, color) {
  ctx.strokeStyle = color;
  ctx.fillStyle = color;
  ctx.lineWidth = 5;
  ctx.beginPath();
  ctx.moveTo(x1, y1);
  ctx.lineTo(x2, y2);
  ctx.stroke();
  const angle = Math.atan2(y2 - y1, x2 - x1);
  ctx.beginPath();
  ctx.moveTo(x2, y2);
  ctx.lineTo(x2 - Math.cos(angle - 0.6) * 18, y2 - Math.sin(angle - 0.6) * 18);
  ctx.lineTo(x2 - Math.cos(angle + 0.6) * 18, y2 - Math.sin(angle + 0.6) * 18);
  ctx.closePath();
  ctx.fill();
}

function roundedPath(points) {
  ctx.beginPath();
  ctx.moveTo(points[0][0], points[0][1]);
  for (let index = 1; index < points.length; index += 1) {
    ctx.lineTo(points[index][0], points[index][1]);
  }
  ctx.closePath();
}

function roundRect(x, y, width, height, radius) {
  ctx.beginPath();
  ctx.moveTo(x + radius, y);
  ctx.arcTo(x + width, y, x + width, y + height, radius);
  ctx.arcTo(x + width, y + height, x, y + height, radius);
  ctx.arcTo(x, y + height, x, y, radius);
  ctx.arcTo(x, y, x + width, y, radius);
  ctx.closePath();
}

function normalizedDelta(a, b) {
  let delta = a - wrap01(b);
  if (delta > 0.5) delta -= 1;
  if (delta < -0.5) delta += 1;
  return delta;
}

function shortestDegreeDelta(a, b) {
  let delta = a - b;
  if (delta > 180) delta -= 360;
  if (delta < -180) delta += 360;
  return delta;
}

function wrap(value, max) {
  return ((value % max) + max) % max;
}

function wrap01(value) {
  return ((value % 1) + 1) % 1;
}

function clamp(value, min, max) {
  return Math.max(min, Math.min(max, value));
}

